// Array Destructuring
const colors = ["Red", "Blue", "Green"];

const [first, second] = colors;

console.log("First Color:", first);
console.log("Second Color:", second);

// Object Destructuring
const employee = {
    name: "Vinay",
    age: 23,
    department: "IT"
};

const { name, age } = employee;

console.log("Name:", name);
console.log("Age:", age);